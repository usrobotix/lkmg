            alert('Переход к оплате заказа');
            // Здесь может быть редирект на страницу оплаты
            // window.location.href = '/payment/process/';
        });
    });

    // Добавляем валидацию для формы отмены заказа
    const cancelLinks = document.querySelectorAll('a[href*="CANCEL=Y"]');
    cancelLinks.forEach(link => {
        link.addEventListener('click', function(event) {
            event.preventDefault();
            const confirmCancel = confirm('Вы уверены, что хотите отменить заказ? Это действие нельзя отменить.');
            if (confirmCancel) {
                window.location.href = this.href;
            }
        });
    });
