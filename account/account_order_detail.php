                lessInfoButtons[index].style.display = 'inline-block';
            };
        });

        document.querySelectorAll('.sale-order-detail-more-info-less').forEach((button, index) => {
            button.onclick = function(e) {
                e.preventDefault();
                moreInfoDetails[index].classList.remove('active');
                button.style.display = 'none';
                moreInfoButtons[index].style.display = 'inline-block';
            };
        });

        // Переопределяем обработчики для отгрузки с анимацией
        document.querySelectorAll('.sale-order-detail-show-link').forEach((link, index) => {
            link.onclick = function(e) {
                e.preventDefault();
                shipmentDetails[index].classList.add('active');
                link.style.display = 'none';
                hideLinks[index].style.display = 'inline-block';
            };
        });

        document.querySelectorAll('.sale-order-detail-hide-link').forEach((link, index) => {
            link.onclick = function(e) {
                e.preventDefault();
                shipmentDetails[index].classList.remove('active');
                link.style.display = 'none';
                showLinks[index].style.display = 'inline-block';
            };
        });
    }

    updateEventHandlers();

    // Добавляем обработчик для кнопки "Сменить способ оплаты"
    const changePaymentLinks = document.querySelectorAll('.sale-order-detail-payment-options-methods-info-change-link');
    changePaymentLinks.forEach(link => {
        link.addEventListener('click', function(event) {
            event.preventDefault();
            alert('Функционал смены способа оплаты будет реализован позже');
        });
    });

    // Добавляем обработчик для кнопки "Оплатить"
    const payButtons = document.querySelectorAll('.active-button');
    payButtons.forEach(button => {
        button.addEventListener('click', function(event) {
